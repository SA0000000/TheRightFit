package com.pearlhacks.therightfit;

public class Person{ 
	public String Name;
	public float Bust;
	public float Waist;
	public float Hip;
	public float Inseam;
	public Person(String inName, float inBust, float inWaist, float inHip, float inInseam){
		this.Name = inName;
		this.Bust = inBust;
		this.Waist = inWaist;
		this.Hip = inHip;
		this.Inseam = inInseam;
	}
}